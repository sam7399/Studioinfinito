-- Studioinfinito Task Manager - Database Setup Script
-- For Supabase PostgreSQL (tables created automatically by Sequelize)

-- If using a local PostgreSQL instance instead of Supabase:
-- CREATE DATABASE task_manager;
-- \c task_manager;

-- If using Supabase:
-- 1. Create a new project at https://supabase.com
-- 2. Go to Settings > Database > Connection string
-- 3. Copy the connection details to your .env file:
--    DBHOST=db.<project-ref>.supabase.co
--    DBNAME=postgres
--    DBUSER=postgres
--    DBPASS=<your-project-password>
--    DBPORT=5432
--    DBSSL=true

-- Note: Tables will be created automatically by Sequelize on startup
-- Or run: npm run db:migrate (from backend directory)
